hari = 3
if (hari==1):
    print ("Hari Senin")
elif (hari==2):
    print ("Hari Selasa")
elif (hari==3):
    print ("Hari Rabu")
elif (hari==4):
    print ("Hari Kamis")
elif (hari==5):
    print ("Hari Jumat")
elif (hari==6):
    print("Hari Sabtu")
else :
    print ("Hari Minggu")             